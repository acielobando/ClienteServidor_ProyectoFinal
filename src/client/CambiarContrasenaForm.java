    public CambiarContrasenaForm(Socket socket, ObjectOutputStream out, ObjectInputStream in, String nombreUsuario) {
        this.socket = socket;
        this.out = out;
        this.in = in;
        this.nombreUsuario = nombreUsuario;

        setTitle("Cambiar Contraseña");
        setSize(400, 300);
        setLocationRelativeTo(null);
        setDefaultCloseOperation(DISPOSE_ON_CLOSE);
        setContentPane(panelPrincipal);

        txtConfirmar.addActionListener(e -> cambiarContrasena());
        btnCancelar.addActionListener(e -> dispose());
    }

    private void cambiarContrasena() {
        try {
            String actual = txtActual.getText();
            String nueva = txtNueva.getText();
            String confirmar = txtConfirmar.getText();

            if (!nueva.equals(confirmar)) {
                Helper.mostrarMensaje("La nueva contraseña y la confirmación no coinciden.");
                return;
            }

            if (nueva.length() < 6 || !nueva.matches(".*[!@#$%^&*()].*")) {
                Helper.mostrarMensaje("La contraseña debe tener al menos 6 caracteres y un carácter especial.");
                return;
            }

            String[] datos = {nombreUsuario, actual, nueva};
            Solicitud solicitud = new Solicitud("cambiar_contrasena", datos);

            out.writeObject(solicitud);
            out.flush();

            Respuesta respuesta = (Respuesta) in.readObject();
            Helper.mostrarMensaje(respuesta.getMensaje());
            if (respuesta.isExito()) {
                dispose();
            }
        } catch (Exception ex) {
            Helper.mostrarMensaje("Error: " + ex.getMessage());
        }
    }

